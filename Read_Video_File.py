import cv2 # مكتبة الرؤية الحاسوبية (OpenCV)
import numpy as np # مكتبة للتعامل مع المصفوفات والأرقام

# ---------------------------------------------------------
# القسم الأول: إعداد الماوس (Mouse Callback)
# ---------------------------------------------------------

# متغيرات عامة (Global) لتخزين إحداثيات الماوس وحالة الضغط
POS_clicked = [0,0] # ستحفظ الإحداثيات [X, Y]
clicked = False     # ستكون True إذا ضغطنا، و False إذا ألغينا التفعيل

# هذه الدالة تراقب حركة الماوس ونقراته باستمرار
def onMouse(event, x, y, flags, param):
    global POS_clicked
    global clicked
    
    # إذا قام المستخدم برفع إصبعه عن الزر "الأيسر" للماوس
    if event == cv2.EVENT_LBUTTONUP:
        POS_clicked = [x,y] # احفظ مكان النقطة التي كان عليها الماوس
        clicked = True      # غيّر الحالة لتشغيل الطباعة لاحقاً
        
    # إذا قام المستخدم بنقر مزدوج بالزر "الأيمن" للماوس
    if event == cv2.EVENT_RBUTTONDBLCLK:
        clicked = False     # أوقف تتبع الإحداثيات

# ---------------------------------------------------------
# القسم الثاني: تجهيز النوافذ والملفات
# ---------------------------------------------------------

# إنشاء نافذة عرض على الشاشة وتسميتها "CAMERA" 
cv2.namedWindow("CAMERA", 1) # 1 تعني cv2.WINDOW_AUTOSIZE (تلقائي الحجم)

# ربط نافذة "CAMERA" بدالة الماوس (لكي تفهم النافذة متى نضغط)
cv2.setMouseCallback('CAMERA', onMouse)

# فتح ملف الفيديو الأصلي للقراءة
CAP = cv2.VideoCapture('Vid.mp4')

# استخراج مواصفات الفيديو (السرعة والأبعاد)
fps = CAP.get(cv2.CAP_PROP_FPS) # سرعة الإطارات في الثانية (Frames per Second)

size = (
       int(CAP.get(cv2.CAP_PROP_FRAME_WIDTH)),  # عرض الإطار
       int(CAP.get(cv2.CAP_PROP_FRAME_HEIGHT))) # طول الإطار

print("Video Size:", size) # طباعة أبعاد الفيديو للتأكد

# إنشاء أداة لحفظ فيديو "جديد" كنسخة (VideoWriter)
vidW = cv2.VideoWriter(
    'MyOutputVid.avi',                        # avi means "Audio Video Interleave" or "تداخل الصوت والفيديو"
    cv2.VideoWriter_fourcc('I','4','2','0'),  # نوع التشفير (Codec) but why we do this? this is because we want to save the video in AVI format, and this is the codec that works well with it. if we want to save in MP4 format, we can use cv2.VideoWriter_fourcc(*'mp4v') instead.
    fps,                                      # السرعة (نفس سرعة الفيديو الأصلي)
    size                                      # الأبعاد (نفس أبعاد الفيديو الأصلي)
)

# ---------------------------------------------------------
# القسم الثالث: تشغيل الفيديو (حلقة التكرار الرئيسية)
# ---------------------------------------------------------

# قراءة أول إطار (صورة) من الفيديو
# success: متغير يكون True إذا نجحت القراءة، و False إذا انتهى الفيديو
# frame: المتغير الذي يحمل الصورة (المصفوفة) نفسها
success, frame = CAP.read()

# طالما أن هناك صور (إطارات) تُقرأ بنجاح، استمر في اللوب:
while success:
    
    # 1. انسخ الإطار الحالي داخل الفيديو الجديد الذي يتم حفظه
    vidW.write(frame)
    
    # 2. اعرض الإطار الحالي في النافذة التي أنشأناها مسبقاً
    cv2.imshow("CAMERA", frame)
    
    # 3. اقرأ الإطار الذي بعده استعداداً للدورة القادمة
    success, frame = CAP.read()
    
    # 4. توقف لمدة 10 ملي-ثانية واقرأ الكيبورد (مهم جداً لكي لا يتجمد الفيديو)
    ch = cv2.waitKey(20)
    
    # (سطر اختياري) طباعة الكود الخاص بالزر الذي يتم ضغطه
    # print (ch) 
    
    # --- أوامر الكيبورد ---
    # إذا تم ضغط زر (ESC) والذي رقمه في نظام ASCII هو 27
    if ch == 27:
        break # اخرج من حلقة الـ while وأنهِ العرض
        
    # إذا تم ضغط حرف 's' والذي رقمه 115 (اختصار Save)
    if ch == 115:
        cv2.waitKey(0) # أوقف الفيديو تماماً حتى تضغط أي زر آخر
        cv2.imwrite('MY_Capture_Image.jpg', frame) # احفظ الإطار كصورة مستقلة
        
    # --- أوامر الماوس ---
    # إذا كانت حالة الضغط مفعّلة (بسبب نقرة الزر الأيسر)
    if clicked == True:
        print (POS_clicked) # اطبع إحداثيات (X,Y) في الـ Terminal بشكل متكرر
        # print (frame[POS_clicked]) # (ملاحظة: هذا السطر سحابي لطباعة لون البكسل)

# ---------------------------------------------------------
# القسم الرابع: التنظيف والإغلاق
# ---------------------------------------------------------

CAP.release()           # إغلاق الفيديو الأصلي وتحريره من الذاكرة
vidW.release()   # إنهاء حفظ الفيديو الجديد
cv2.destroyAllWindows() # إغلاق جميع نوافذ العرض على الشاشة